# começa com zero porque nenhuma área foi analisada ainda
riscos_por_area = [0, 0, 0, 0, 0]

# percorre todos os ciclos da matriz dados_missao
for i in range(len(dados_missao)):
    risco = mostrar_ciclo(i + 1, dados_missao[i], riscos_por_area)

    # guarda o risco calculado na lista de riscos
    riscos.append(risco)

# gerar o relatório final
gerar_relatorio_final(riscos, riscos_por_area)